package com.picsy.model;

/**
 * @author samuelkirton
 */
public class CropResult {
	private int x;
	private int y;
	private int width;
	private int height;
	
	public int getX() {
		return x;
	}
	
	public void setX(int newVal) {
		x = newVal;
	}

	public int getY() {
		return y;
	}

	public void setY(int newVal) {
		y = newVal;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int newVal) {
		width = newVal;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int newVal) {
		height = newVal;
	}
}